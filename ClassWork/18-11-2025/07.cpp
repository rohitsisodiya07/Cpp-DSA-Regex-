#include<bits/stdc++.h>
using namespace std;

int main()
{
	//switch
	char ch;
	cout << "Enter Character = ";
	cin >> ch ;
	switch(ch){

	    case 'a':
	        cout << "Namaste";
	        break;
	    case 'b':
	        cout << "Hello";
	        break;
	    case 'c':
	        cout << "Vadakam";
	        break;
	    case 'd':
	        cout << "KemChoo";
	        break;
	    case 'e':
	        cout << "Kedoaalaye";
	        break;
	    case 'f':
	        cout << "Jai Shree Ram!!";
	        break;
	    default:
	        cout << "Enter Valid Input";
	        break;
	}
}